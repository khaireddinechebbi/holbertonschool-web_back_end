#!/usr/bin/env python3
"""
This script defines a function that returns the string \
representation of a float.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation of a float.

    Parameters:
    n (float): The float value to convert to a string.

    Returns:
    str: The string representation of the float.
    """
    return str(n)
