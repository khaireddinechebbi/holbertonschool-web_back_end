#!/usr/bin/env python3
"""
This script take to floats and returns their sum as a float
"""
def add(a: float, b: float) -> float:
    """
    Returns the sum of two floats.

    Parameters:
    a (float): The first float.
    b (float): The second float.

    Returns:
    float: The sum of the two floats.
    """
    return a + b
